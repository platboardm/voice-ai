// applyCMVN normalises a single frame of fbank features in-place.
// out[d] = (feat[d] - mean[d]) * istd[d]
func applyCMVN(feat []float32) {
	for d := 0; d < featDim && d < len(feat); d++ {
		feat[d] = (feat[d] - cmvnMeans[d]) * cmvnISTD[d]
	}
}
